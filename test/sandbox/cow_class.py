
class Cow:

    def __init__(id, rf_id, weight, spray_period, next_spray_time, last_drink_duration):
        self.id = id
        self.rf_id = rf_id
        self. weight = weight
        self.spray_period = spray_period
        self.next_spray_period = next_spray_time
        self.last_drink_duration = last_drink_duration

@property
def cow_id(self):
    return '{}.{} cow'.format(self.rf_id, self.weight)

